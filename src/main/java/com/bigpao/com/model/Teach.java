public class Teach {
    private int teachId;
    private String teachName;
    private String teachEmail;
    private String teachIntroduce;
    private String teachHeadurl;

    public int getTeachId() {
        return teachId;
    }

    public void setTeachId(int teachId) {
        this.teachId = teachId;
    }

    public String getTeachName() {
        return teachName;
    }

    public void setTeachName(String teachName) {
        this.teachName = teachName;
    }

    public String getTeachEmail() {
        return teachEmail;
    }

    public void setTeachEmail(String teachEmail) {
        this.teachEmail = teachEmail;
    }

    public String getTeachIntroduce() {
        return teachIntroduce;
    }

    public void setTeachIntroduce(String teachIntroduce) {
        this.teachIntroduce = teachIntroduce;
    }

    public String getTeachHeadurl() {
        return teachHeadurl;
    }

    public void setTeachHeadurl(String teachHeadurl) {
        this.teachHeadurl = teachHeadurl;
    }
}
